export function getRandomScript(exclude?: string): LogicScript {
  const pool = exclude ? LOGIC_SCRIPTS.filter((s) => s.id !== exclude) : LOGIC_SCRIPTS
  return pool[Math.floor(Math.random() * pool.length)]
}

// ─── Diff helpers ─────────────────────────────────────────────────────────────

export interface DiffToken {
  char: string
  correct: boolean
}

export function diffText(userInput: string, original: string): DiffToken[] {
  return userInput.split("").map((char, i) => ({
    char,
    correct: original[i] === char,
  }))
}

export function calcAccuracy(userInput: string, original: string): number {
  if (!userInput.length || !original.length) return 0
  let correct = 0
  const comparable = Math.min(userInput.length, original.length)
  for (let i = 0; i < comparable; i++) {
    if (userInput[i] === original[i]) correct++
  }
  return Math.round((correct / original.length) * 100)
}
