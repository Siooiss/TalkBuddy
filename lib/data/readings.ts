export function getRandomReading(exclude?: string): ReadingText {
  const pool = exclude ? READING_TEXTS.filter((t) => t.id !== exclude) : READING_TEXTS
  return pool[Math.floor(Math.random() * pool.length)]
}

// ─── Levenshtein-based scoring ────────────────────────────────────────────────

/** Space-efficient Levenshtein distance */
export function levenshtein(a: string, b: string): number {
  const m = a.length
  const n = b.length
  const dp = Array.from({ length: n + 1 }, (_, i) => i)
  for (let i = 1; i <= m; i++) {
    let prev = dp[0]
    dp[0] = i
    for (let j = 1; j <= n; j++) {
      const tmp = dp[j]
      dp[j] =
        a[i - 1] === b[j - 1] ? prev : 1 + Math.min(prev, dp[j], dp[j - 1])
      prev = tmp
    }
  }
  return dp[n]
}

/** Strip punctuation/whitespace for fair comparison */
function clean(s: string): string {
  return s.replace(/[，。！？、；：""''（）【】…—\s\n]/g, "")
}

/**
 * Returns 0 if recognized is empty; otherwise similarity % vs original.
 */
export function calcClarity(recognized: string, original: string): number {
  if (!recognized.trim()) return 0
  const a = clean(recognized)
  const b = clean(original)
  if (!a || !b) return 0
  const dist = levenshtein(a, b)
  return Math.max(0, Math.round((1 - dist / b.length) * 100))
}

/**
 * Pace score: based on actual speaking duration vs estimated optimal duration.
 * Optimal Chinese reading pace: ~4.5 chars / second.
 */
export function calcPace(recognizedChars: number, durationSec: number): number {
  if (durationSec < 1 || recognizedChars === 0) return 0
  const actualCPS = recognizedChars / durationSec
  const optimalCPS = 4.5
  const ratio = actualCPS / optimalCPS
  // Ideal ratio = 1.0; too fast (>1.5) or too slow (<0.5) lowers score
  const deviation = Math.abs(ratio - 1)
  return Math.max(0, Math.round(100 - deviation * 70))
}

/**
 * Pause score: proxy based on sentence completeness and clarity.
 * Returns 0 if not spoken.
 */
export function calcPause(recognized: string, original: string, clarity: number): number {
  if (!recognized.trim()) return 0
  const origLen = clean(original).length
  const recogLen = clean(recognized).length
  const lengthRatio = Math.min(recogLen / Math.max(origLen, 1), 1)
  // Blend clarity and length coverage
  return Math.max(0, Math.min(100, Math.round(clarity * 0.7 + lengthRatio * 30)))
}

/** Generate actionable suggestions based on actual scores */
export function generateReadingSuggestions(
  clarity: number,
  pace: number,
  pause: number,
): string[] {
  const suggestions: string[] = []

  if (clarity === 0) {
    suggestions.push("未能识别到有效语音，请确认麦克风权限已开启，并在安静环境中清晰朗读。")
    return suggestions
  }

  if (clarity < 60) {
    suggestions.push(
      "发音清晰度有较大提升空间。建议每次朗读前先默读一遍，标注难发音的字词，并放慢整体语速，让每个音节都完整发出。",
    )
  } else if (clarity < 80) {
    suggestions.push(
      "发音整体清晰，个别字词稍有偏差。可重点关注前后鼻音（in/ing、an/ang）和平翘舌音（z/zh、c/ch、s/sh）的准确区分。",
    )
  } else {
    suggestions.push(
      "发音清晰度优秀！继续保持咬字清晰的好习惯，下一步可以在保持清晰度的同时，提升语调的抑扬顿挫感。",
    )
  }

  if (pace < 50) {
    suggestions.push(
      "语速与原文节奏差距较大。建议参考原文的长句结构，在逗号处做短停顿，在句号处做稍长停顿，保持匀速推进。",
    )
  } else if (pace < 75) {
    suggestions.push(
      "语速有一定起伏。尝试朗读时轻声「打拍子」，保持稳定的节奏感，避免在复杂句式处突然加速或停顿过长。",
    )
  }

  if (pause < 60) {
    suggestions.push(
      "断句方面仍有提升余地。建议先通读全文，理解句子的意群（即意思上的组合），再按照意群划分来换气断句，这样会比按标点符号停顿更自然流畅。",
    )
  }

  if (suggestions.length < 2) {
    suggestions.push(
      "整体表现良好！可以尝试加入更多情感色彩：在描述性词汇上适当延伸音长，在转折词（「然而」「但是」）前微微降调，让朗读更有层次感和感染力。",
    )
  }

  return suggestions
}
